package com.br.projetoat.model;

public class ModelQuestionario {

    private int notaTempo;
    private int notaAtendimento;
    private int notaResulucao;

    public int getNotaTempo() {
        return notaTempo;
    }

    public void setNotaTempo(int notaTempo) {
        this.notaTempo = notaTempo;
    }

    public int getNotaAtendimento() {
        return notaAtendimento;
    }

    public void setNotaAtendimento(int notaAtendimento) {
        this.notaAtendimento = notaAtendimento;
    }

    public int getNotaResulucao() {
        return notaResulucao;
    }

    public void setNotaResulucao(int notaResulucao) {
        this.notaResulucao = notaResulucao;
    }
}
